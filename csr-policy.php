												13. Planning, implementation & monitoring
											</div>
										</div>
										<div class="accordion-content" style="display: none;">
											The CSR policy is implemented as per the Act and the Rules. The CSR policy will be implemented ensuring that all projects/programmes as budgeted are duly carried out. CSR spends will be closely monitored and funds shall be released against verified utilizations as per the approved work plans. This may include monthly field visits, comprehensive documentation, and regular interaction with beneficiary communities. Further, CSR spends will be audited in an accountable and transparent manner.
											<p></p>
											<p>In order to fulfill the objectives, the various CSR activities shall be assigned to different groups, each headed by a Convener along with supporting members. It shall also be ensured to include few local representatives as link-man/coordinator in the sub-committee. For, projects in villages, persons/representatives from Villages/Gram Sabha’s / Ward members shall also be engaged. This will help in achieving the target as well as participation of the local representatives in formulating the various activities to obtain maximum benefit by community.</p>
											<p>Activities shall, as far as possible, be implemented in a project mode. For easy implementation, long-term CSR projects shall be broken down into medium-term and short-term plans. Each plan shall specify the CSR activities planned to be undertaken for each year. Accordingly, the budget shall be allocated for the implementation of these activities and achievement of targets set for each successive year, till the final completion of the project.</p>
											<p>Projects especially the construction/civil activities and other projects which require specialized knowledge and skills shall be executed by the external specialized agencies. Specialized agencies may include Government departments, semi government, or non-government organizations (NGOs), autonomous organizations, professional consultancy organizations, registered Trusts/ Missions, community based organizations, self-help groups, not-for-profit organizations, local bodies such as Panchayat Raj institutions, academic institutes, etc. Engagement of external specialized agencies/NGOs shall be normally from the available panels of such agencies maintained by the Government Ministries/Departments, Planning Commission, autonomous organizations, or the National/Regional CSR Hub or as per the Standard purchase procedure/entering into a MoU.</p>
											<p>Further, close co-ordination shall also be done with Central/State/Local Governments/NGO’s to synergize the activities with their developmental programmes.</p>
											<p>All the CSR activities shall be monitored regularly by Chairman, CSR Committee and In-Charge, CSR Cell. Review discussions shall be held amongst the CSR Cell employees/committee members and suggestions for further improvements shall be discussed in details. Feedback shall be regularly obtained from local area representative/link-man / coordinator about benefits of various activities and/or their suggestions. The convener of the committee shall also work in close liaison with them and ensure that benefits of all the activities reaches to the total cross section of the community.</p>

