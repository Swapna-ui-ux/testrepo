								<h2>Code of Conduct</h2>
								  <h4>Introduction</h4>
									<ul class="listitems">
										<li>This code of Conduct (“this Code”) shall be called “The Code of Conduct for Board Members and Senior Management Personnel” of Aurobindo Pharma Limited (hereinafter referred to as “the Company”).</li>
										<li>This code has been framed specifically in compliance with the provisions of the listing agreement entered into with stock exchanges.</li>
										<li>The purpose of this code is to enhance further an ethical and transparent process in managing the affairs of the Company.</li>
										<li>It has come in to force with effect from December 31st, 2005.</li>
									</ul>	

                                  <h4>Definition &amp; Interpertations</h4>
									<ul class="listitems">
										<li>The term “Board Members” shall mean Directors on the Board of Directors of the Company.</li>
										<li>The term “Whole-time Director” shall be the Directors on the Board of Directors of the Company who are in whole-time employment of the Company.</li>
										<li>The term “Part-time Director” shall mean Directors on the Board of Directors of the Company who are not in whole time employment of the Company.</li>
										<li>The term “Senior Management Personnel” shall mean personnel of the Company in the cadre / designation of Senior Manager and above, excluding Board of Directors. Such personnel shall hereinafter be treated as members of its core management team.</li>
										<li>The term “Relative” shall have the same meaning as defined in Section 2(77) of the Companies Act, 2013.</li>
									</ul>

									<h4>Applicability</h4>
									<p>This code shall be applicable to the following persons:</p>
									<ul class="listitems">
										<li>All whole-time Directors including the Chairman of the Company.</li>
										<li>All part-time Directors.</li>
										<li>All Senior Management Personnel.</li>
									</ul>

									<h4>Conflicts of Interest</h4>
									<ul class="listitems">
										<li>The Directors and senior management should be scrupulous in avoiding ‘conflicts of interest’ with the Company. This is an area in which it is impossible to provide comprehensive guidance, but the guiding principle is that any event, activity or situation involving conflict or potential conflict of interest must be disclosed to the Board for guidance and appropriate action.</li>
									</ul>

									<h4>Honest and Ethical Conduct</h4>
										<ul class="listitems">
											<li>The Directors and senior management shall act in accordance with the highest standards of personal and professional integrity, honesty and ethical conduct and use their powers of office, in good faith and in the best interests of the Company as a whole.</li>
										</ul>

										<h4>Corporate Opportunities</h4>
										<ul class="listitems">
											<li>The Directors and senior management owe a duty to the Company to advance its legitimate interests when the opportunity to do so arises and are expressly prohibited from improper use of information or property or, taking improper advantage of their position.</li>
										</ul>

										<h4>Compliance With Laws, Rules and Regulations</h4>
										<ul class="listitems">
											<li>The Directors and senior management shall endeavor to ensure compliance with all applicable laws, rules, and regulations applicable to the Company. Transactions, directly or indirectly, involving securities of the Company should not be undertaken without complying with the Code of Conduct for Prohibition of Insider Trading.</li>
										</ul>

										<h4>Confidentiality</h4>
										<ul class="listitems">
											<li>The Directors and senior management shall maintain the confidentiality of information of the Company or that of any customer, supplier or business associate of the Company to which the Company has a duty to maintain confidentiality, except when disclosure is authorized or legally mandated. The Confidential information includes all non-public information (including private, proprietary, and other) that might be of use to competitors or the disclosure of which might be harmful to the company or its Associates. The use of confidential information for his/ her own advantage or profit is also prohibited.</li>
										</ul>

										<h4>Protection and Proper Use of Company’s Assets</h4>
										<ul class="listitems">
											<li>The Directors and senior management should protect the company’s assets and property. The company’s assets should be used only for legitimate business purposes.</li>
										</ul>

										<h4>Compliance With Code of Conduct</h4>
										<ul class="listitems">
											<li>All Directors and senior management personnel shall adhere to this code of conduct and affirm compliance with the code on an annual basis, as per the requirements of the Listing Agreement with the Stock Exchanges. Violation of this Code will lead to appropriate action.</li>
										</ul>

										<h4>The Duties of Independent Directors Shall Be to</h4>
									<ul class="listitems">
										<li>Undertake appropriate induction and regularly update and refresh their skills, knowledge and familiarity with the company.</li>
										<li>Seek appropriate clarification or amplification of information and, where necessary, take and follow appropriate professional advice and opinion of outside experts at the expense of the company.</li>
										<li>Strive to attend all meetings of the Board of Directors and of the Board committees of which he is a member.</li>
										<li>Participate constructively and actively in the committees of the Board in which they are chairpersons or members.</li>
										<li>Strive to attend the general meetings of the company</li>
										<li>Where they have concerns about the running of the company or a proposed action, ensure that these are addressed by the Board and, to the extent that they are not resolved, insist that their concerns are recorded in the minutes of the Board meeting</li>
										<li>Keep themselves well informed about the company and the external environment in which it operates.</li>
										<li>Not to unfairly obstruct the functioning of an otherwise proper Board or committee of the Board.</li>
										<li>Pay sufficient attention and ensure that adequate deliberations are held before approving related party transactions and assure themselves that the same are in the interest of the company.</li>
										<li>Ascertain and ensure that the company has an adequate and functional vigil mechanism and to ensure that the interests of a person who uses such mechanism are not prejudicially affected on account of such use</li>
										<li>Report concerns about unethical behaviour, actual or suspected fraud or violation of the company’s code of conduct or ethics policy.</li>
										<li>Acting within his authority, assist in protecting the legitimate interests of the company, shareholders and its employees.</li>
										<li>Not disclose confidential information, including commercial secrets, technologies, advertising and sales promotion plans, unpublished price sensitive information, unless such disclosure is expressly approved by the Board or required by law.</li>
									</ul>

